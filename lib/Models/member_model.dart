class Member {
  final String name;
  final String mobile;
  final String plan;
  final String status;
  final String joinDate;
  final String expiryDate;
  final int attendance;

  Member({
    required this.name,
    required this.mobile,
    required this.plan,
    required this.status,
    required this.joinDate,
    required this.expiryDate,
    required this.attendance,
  });
}
